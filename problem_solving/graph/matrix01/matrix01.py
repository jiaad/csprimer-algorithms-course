def updateMatrix(mat: List[List[int]]) -> List[List[int]]:
    mem = {}

    def find_shortest(mat, row, col):
        visited = set()
        q = [[row, col, 0]]

        while len(q):
            r, c, depth = q.pop(0)
            rowInbound = r >= 0 and r < len(mat)
            colInbound = c >= 0 and c < len(mat[0])
            if not rowInbound or not colInbound:
                continue
            if (str(r) + "," + str(c)) in visited:
                continue
            visited.add(str(r) + "," + str(c))

            rckey = str(r) + "," + str(c)
            if mem.get(rckey, None) != None:
                res = mem.get(rckey, 0)
                if depth < mem.get(rckey, 0):
                    res = mem.get(rckey, 0) - depth
                elif depth > mem.get(rckey, 0):
                    res = mem.get(rckey, 0) + depth
                mat[row][col] = res
                mem[str(row) + "," + str(col)] = res
                break

            if mat[r][c] == 0:
                mat[row][col] = depth
                mem[str(row) + "," + str(col)] = depth
                break

            q.append([r - 1, c, depth + 1])
            q.append([r + 1, c, depth + 1])
            q.append([r, c - 1, depth + 1])
            q.append([r, c + 1, depth + 1])
        return mat

    # visited = set()
    # current = None  # le 1
    # q = []
    for row in range(0, len(mat)):
        for col in range(0, len(mat[0])):
            if mat[row][col] == 0:
                continue
            # if (str(row) + "," + str(col)) in visited: continue
            visited = set()
            # q.append([row, col, 1])
            find_shortest(mat, row, col)
    return mat


